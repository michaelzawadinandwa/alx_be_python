# future_age_calculator.py
# This script calculates how old the user will be in the year 2050.

# Prompt the user to enter their current age
current_age = int(input("How old are you? "))

# Calculate the user's age in 2050
future_age = current_age + 27  # Since 2050 - 2023 = 27 years

# Display the result
print("In 2050, you will be", future_age, "years old.")